class L3_15dowhile{
	public static void main(String[] args){
		
		int n = 3;//linha
		comprimento (n);
		int N;
		
		
		
	}	
		
	static void comprimento(int n){
		int N=0;		
		
		do {
			System.out.print("*");
			N++;
		} while (N<n);
	
		
		if (n<0){
			System.out.println("erro n >= 0");
		}
	}	
	
	
	
}



