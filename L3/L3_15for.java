class L3_15for{
	public static void main(String[] args){
		
		int n = -3;//linha
		comprimento (n);		
	}	
		
		
		
	static void comprimento(int n){
		
		int N;
		
		for (N = 0; N < n; N++ ){
			System.out.print("*");
		}	
		
		if (n<0){
			System.out.println("erro n >= 0");
		}
	}	
	
	
}

