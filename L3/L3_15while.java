class L3_15while{
	public static void main(String[] args){
		
		int n = 3;//linha
		Comprimento (n);

		
	}	
		
	static void Comprimento(int n){
		int N = 0;
		
		while (N<n){
			System.out.print("*");
			N++;
		}
		
		if (n<0){
			System.out.println("erro n >= 0");
		}
	}	
	
	
}



